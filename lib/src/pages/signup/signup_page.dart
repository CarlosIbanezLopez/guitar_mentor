                        controller: _cvvController,
                        validator: (value) {
                          if (_cvvController.text.isEmpty) {
                            return "This field can't be empty";
                          }
                          return null; // Added to remove warning
                        },
                        obscureText: true,
                        style: GoogleFonts.poppins(
                          color: ThemeColors.whiteTextColor,
